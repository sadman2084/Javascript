document.write("Using for loop  <br><br>");
for (var i = 0; i < 5; i++) {
  document.write("Hardware geeks <br>");
}
document.write("<br><br>");

document.write("Using while loop  <br><br>");
var i = 0;
while (i < 5) {
  document.write("Hardware geeks <br>");
  i++;
}
document.write("<br><br>");

document.write("Using do while loop  <br><br>");
var i = 0;
do {
  document.write("Hardware geeks <br>");
  i++;
} while (i < 5);
